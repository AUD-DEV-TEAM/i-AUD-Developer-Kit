/**
 * DataGrid/TreeGrid 헤더 스타일 옵션
 */
export interface HeaderStyleOption{

  /**
   * 그리드 컨트롤 Column 헤더 높이
   */
  ColumnHeaderHeight: number;

  /**
   * 그리드 컨트롤 Column 헤더 박스 스타일 키
   */
  ColumnHeaderStyle: string;

  /**
   * 그리드 컨트롤 Row 헤더 박스 스타일 키
   */
  RowHeaderStyle: string;

  /**
   * 그리드 컨트롤 Row 헤더 너비
   */
  RowHeaderWidth: number;

}
