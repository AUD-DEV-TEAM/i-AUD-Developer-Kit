/**
* 차트의 제목 스타일 정보를 제공합니다.
*/
export interface Style{

  /**
   * 글자 색
  */
  FontColor: string;

  /**
   * 글자 유형
  */
  FontFamily: string;

  /**
   * 글자 크기
  */
  FontSize: number;

  /**
   * 글자 스타일(default:normal, italic)
  */
  FontStyle: string;

  /**
   * 글자 굵기(default:normal, bold)
  */
  FontWeight: string;

}
