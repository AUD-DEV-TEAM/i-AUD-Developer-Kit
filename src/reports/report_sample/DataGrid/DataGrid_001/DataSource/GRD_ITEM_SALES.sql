SELECT OI.PRODUCT_ID AS "상품 번호"
	 , O.ORDER_ID AS "주문 번호"
	 , O.ORDER_STATUS AS "주문 상태"
     , OP.PAYMENT_TYPE AS "결제 수단"
	 , OP.PAYMENT_INSTALLMENTS AS "할부 개월 수"
	 , OP.PAYMENT_VALUE AS "결제 금액"
	 , O.ORDER_PURCHASE_TIMESTAMP AS "결제 시각"
FROM OLIST_ORDERS	O
LEFT JOIN OLIST_ORDER_PAYMENTS	OP
ON O.ORDER_ID = OP.ORDER_ID
LEFT JOIN OLIST_ORDER_ITEMS	OI
ON O.ORDER_ID = OI.ORDER_ID
WHERE O.ORDER_PURCHASE_TIMESTAMP BETWEEN :VS_YMD_FROM AND :VS_YMD_TO
ORDER BY OP.PAYMENT_VALUE DESC