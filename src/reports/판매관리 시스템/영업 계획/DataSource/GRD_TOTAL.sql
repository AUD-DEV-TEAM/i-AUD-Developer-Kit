SELECT TO_CHAR(COUNT(DISTINCT SP.PLAN_ID), 'FM999,999,999,999')||'건'	AS TOTAL_PLAN_COUNT
	 , TO_CHAR(COUNT(CASE 
	 			WHEN SP.PLAN_STATUS = 'CONFIRMED' THEN 1 
			 END), 'FM999,999,999,999')||'건' 							AS TOTAL_FIXED_PLAN
	 , TO_CHAR(SUM(SP.TARGET_QTY), 'FM999,999,999,999') 				AS TOTAL_PLAN_QTY
	 , '₩'||TO_CHAR(SUM(SP.TARGET_AMT), 'FM999,999,999,999') 			AS TOTAL_PLAN_AMT
  FROM SM_SALES_PLAN	SP