SELECT C.CUST_ID
	 , C.CUST_NAME
	 , CT.CODE_NAME		AS CUST_TYPE
	 , CG.CODE_NAME		AS CUST_GRADE
	 , C.BIZ_REG_NO
	 , C.PHONE
  FROM SM_CUSTOMER C
  LEFT JOIN SM_COMMON_CODE CT
	ON CT.GROUP_CD = 'CUST_TYPE'
   AND C.CUST_TYPE = CT.CODE
  LEFT JOIN SM_COMMON_CODE CG
	ON CG.GROUP_CD = 'CUST_GRADE'
   AND C.CUST_GRADE = CG.CODE
 WHERE 1=1
   AND (C.CUST_NAME LIKE %:VS_KEYWORD%
	 OR C.BIZ_REG_NO LIKE %:VS_KEYWORD%)
 ORDER BY C.CUST_NAME
