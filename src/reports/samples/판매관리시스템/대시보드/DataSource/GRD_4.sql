SELECT ROW_NUMBER() OVER (ORDER BY SUM(PFM.NET_AMOUNT) DESC) AS RANK
	 , E.EMP_NAME AS EMP_NAME
	 , E.JOB_ROLE AS JOB_ROLE
	 , SUM(PFM.NET_AMOUNT) AS ACTUAL
FROM SM_SALES_PERFORMANCE	PFM
LEFT JOIN SM_EMPLOYEE	E
	ON PFM.EMP_ID = E.EMP_ID
 WHERE TO_CHAR(PFM.SALES_DATE,'YYYY') = :VS_YEAR
 GROUP BY E.EMP_ID
 LIMIT 5