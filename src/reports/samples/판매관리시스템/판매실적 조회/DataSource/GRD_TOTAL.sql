SELECT COUNT(DISTINCT SP.SALES_ID)										AS TOTAL_COUNT
	 , TO_CHAR(SUM(SP.QTY), 'FM999,999,999,999')						AS TOTAL_QTY
	 , TO_CHAR(SUM(SP.NET_AMOUNT), 'FM999,999,999,999')				AS TOTAL_AMT
	 , TO_CHAR(SUM(SP.COST_AMOUNT), 'FM999,999,999,999')				AS TOTAL_COST
  FROM SM_SALES_PERFORMANCE	SP
 WHERE 1=1
   AND SP.SALES_DATE BETWEEN @:VS_YMD_FROM AND @:VS_YMD_TO
   AND SP.CUST_ID IN (@:VS_CUST_IDS)
   AND SP.EMP_ID IN (@:VS_PIC)
   AND SP.SALES_STATUS IN (@:VS_STATUS)
