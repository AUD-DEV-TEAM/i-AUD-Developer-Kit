SELECT   T1.LAYOUT_CODE  
 		, T1.USER_CODE  
		, T2.USER_NAME
 		, T1.REPORT_CODE  
		, T1.CONTROL_CODE
		, T3.REPORT_NAME
 		, T1.LAYOUT_NAME  
 		, T1.LAYOUT_INFO   
 		, T1.CREATE_DATE
		, '[삭제]' AS DEL_BTN
 FROM MEX_USER_REPORT_LAYOUT T1 
 INNER JOIN MTX_USER T2 ON T1.USER_CODE = T2.USER_CODE
 INNER JOIN MTX_REPORT T3 ON T1.REPORT_CODE = T3.REPORT_CODE
 WHERE T1.REPORT_CODE = :VS_LAYOUT_REPORT_CODE
   AND T1.CONTROL_CODE = :VS_CONTROL_CODE
 ORDER BY CREATE_DATE DESC