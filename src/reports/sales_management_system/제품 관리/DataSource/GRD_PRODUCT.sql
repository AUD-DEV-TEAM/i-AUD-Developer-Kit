SELECT P.PROD_ID			
	 , P.PROD_NAME
	 , CG.CODE_NAME	 AS CATEGORY		
	 , P.STD_PRICE
	 , P.COST_PRICE
	 , CG2.CODE_NAME AS STD_UNIT_NAME
	 , P.CATEGORY 	 AS CATEGORY_CODE
	 , P.STD_UNIT	
  FROM SM_PRODUCT P
LEFT JOIN SM_COMMON_CODE CG
	   ON CG.GROUP_CD = 'PROD_CATEGORY'
	  AND P.CATEGORY = CG.CODE
LEFT JOIN SM_COMMON_CODE CG2
	   ON CG2.GROUP_CD = 'STD_UNIT'
	  AND P.STD_UNIT = CG2.CODE
WHERE 1=1
  AND CG.CODE IN (@:VS_CAT)			
  AND (P.PROD_ID LIKE %:VS_KEYWORD%
   OR P.PROD_NAME LIKE %:VS_KEYWORD%)
ORDER BY P.PROD_ID