SELECT TO_CHAR(COUNT(DISTINCT SP.SALES_ID), 'FM999,999,999,999')||'건'	AS TOTAL_PERF_COUNT
	 , TO_CHAR(COUNT(CASE
	 			WHEN SP.SALES_STATUS = 'COMPLETED' THEN 1
				END), 'FM999,999,999,999')||'건' 						AS TOTAL_COMP_PERF
	 , TO_CHAR(SUM(SP.QTY), 'FM999,999,999,999') 						AS TOTAL_QTY
	 , '₩'||TO_CHAR(SUM(SP.NET_AMOUNT), 'FM999,999,999,999') 			AS TOTAL_AMT
  FROM SM_SALES_PERFORMANCE	SP